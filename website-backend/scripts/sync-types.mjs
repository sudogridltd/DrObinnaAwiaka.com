/**
 * sync-types.mjs
 *
 * Generates Strapi TypeScript types and syncs them to the frontend project.
 *
 * Usage:
 *   node scripts/sync-types.mjs
 *   # or via npm script:
 *   npm run sync-types
 *
 * What it does:
 *   1. Runs `strapi ts:generate-types` to produce types/generated/
 *   2. Writes website/src/types/strapi.ts with all typed interfaces
 */

import { execSync } from 'child_process';
import { writeFileSync, mkdirSync, existsSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const FRONTEND_ROOT = resolve(ROOT, '..', 'website');
const FRONTEND_TYPES_DIR = resolve(FRONTEND_ROOT, 'src', 'types');

console.log('🔄 Generating Strapi TypeScript types...');

try {
  execSync('npm run strapi -- ts:generate-types', {
    cwd: ROOT,
    stdio: 'inherit',
  });
  console.log('✅ Types generated successfully');
} catch (err) {
  console.error('❌ Failed to generate types:', err.message);
  process.exit(1);
}

if (!existsSync(FRONTEND_TYPES_DIR)) {
  mkdirSync(FRONTEND_TYPES_DIR, { recursive: true });
  console.log(`📁 Created ${FRONTEND_TYPES_DIR}`);
}

