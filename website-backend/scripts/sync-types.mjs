/** Extract plain string array from a StrapiTextItem component list */
export function extractTextItems(items: StrapiTextItem[] | undefined | null): string[] {
  return items?.map(i => i.text) ?? [];
}

/** Map StrapiDisabledDay names to JS day-of-week numbers (0 = Sunday) */
export function disabledDaysToNumbers(days: StrapiDisabledDay[] | undefined | null): number[] {
  const map: Record<StrapiDisabledDayName, number> = {
    Sunday: 0, Monday: 1, Tuesday: 2, Wednesday: 3,
    Thursday: 4, Friday: 5, Saturday: 6,
  };
  return days?.map(d => map[d.day]) ?? [];
}
